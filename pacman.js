function bfs(grafo, inicio, meta) {
  var fila = [inicio];
  var visitados = [inicio];
  var parentes = {};

  while (fila.length > 0) {
    var no = fila.shift();

    if (no === meta) {
      var caminho = [meta];

      while (meta !== inicio) {
        caminho.unshift(parentes[meta]);
        meta = parentes[meta];
      }
      return caminho;
    }

    for (var i = 0; i < grafo[no].length; i++) {
      var vizinho = grafo[no][i];
      if (!visitados.includes(vizinho)) {
        visitados.push(vizinho);
        fila.push(vizinho);
        parentes[vizinho] = no;
      }
    }
  }
  return false;
}

